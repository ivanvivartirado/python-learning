estadisticas = input ("Introduce las estadisticas: ")

print ("El total de las estadisticas es: ", sum(estadisticas))
print ("La cantidad de estadisticas es: ", len(estadisticas))
print ("El numero mas alto de las estadisticas es: ", max(estadisticas))
print ("El numero mas bajo de las estadisticas es: ", min(estadisticas))
print ("La media de las estadisticas es: ", sum(estadisticas)/len(estadisticas))