# AND Operador logico realiza la operacion logica Y si ambas condiciones son verdaderas devuelve verdadero y si no devuelve falso
print(True and True)
print(True and False)
print(False and True)
print(False and False)

# OR Operador logico realiza la operacion logica O si al menos una condicion es verdadera devuelve verdadero y si no devuelve falso
print(True or True)
print(True or False)
print(False or True)
print(False or False)   

# NOT es un operador logico que invierte el valor de la condicion, si es verdadera la devuelve falsa y si es falsa la devuelve verdadera
print(not True)
print(not False)